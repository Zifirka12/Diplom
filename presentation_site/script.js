function showPage(page) {
  document.querySelector('.container').classList.add('hidden');
  document.getElementById('presentation-page').classList.add('hidden');
  document.getElementById('speaker-page').classList.add('hidden');

  if (page === 'presentation') {
    const pres = document.getElementById('presentation-page');
    pres.innerHTML = presentationContent;
    pres.classList.remove('hidden');
  } else if (page === 'speaker') {
    const sp = document.getElementById('speaker-page');
    sp.innerHTML = speakerContent;
    sp.classList.remove('hidden');
  }
} 